#include <stdio.h>

int main(){

int a = 10; 
float b = 1.1;
char c = 'a';
double d = 12.3;

printf("%d This is Integer.\n", a); //%d is Format Specifier For Int.
printf("%.2f This is float.\n", b);//%f is Format Specifier For Float.
printf("%c This is Char.\n", c);//%c is Format Specifier For Char.
printf("%.2lf This is Double.\n", d);//%lf is Format Specifier For double.



return 0;
}
